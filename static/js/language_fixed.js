function changeLanguage(lang) {
    console.log('🌐 Changing language to:', lang);
    
    try {
        // Save preference
        localStorage.setItem('preferredLanguage', lang);
        
        const trans = translations[lang] || translations.en;
        
        // Update all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(el => {
            const key = el.getAttribute('data-translate');
            if (trans[key]) {
                if (el.tagName === 'INPUT' && el.placeholder) {
                    el.placeholder = trans[key];
                } else {
                    el.textContent = trans[key];
                }
            }
        });
        
        // Update metric labels
        const metricLabels = document.querySelectorAll('.metric-label');
        const metricKeys = ['recommended_crop', 'confidence_score', 'predicted_yield', 'estimated_profit', 'soil_health', 'water_efficiency'];
        metricLabels.forEach((el, index) => {
            if (metricKeys[index] && trans[metricKeys[index]]) {
                el.textContent = trans[metricKeys[index]];
            }
        });
        
        // Update sidebar navigation
        const sidebarItems = document.querySelectorAll('.sidebar .list-group-item');
        const navKeys = ['dashboard', 'crops', 'soil', 'weather', 'irrigation', 'fertilizer', 'market', 'finance', 'labor', 'equipment'];
        sidebarItems.forEach((el, index) => {
            if (navKeys[index] && trans[navKeys[index]]) {
                const icon = el.querySelector('i');
                if (icon) {
                    el.innerHTML = icon.outerHTML + ' ' + trans[navKeys[index]];
                }
            }
        });
        
        // Update language dropdown display
        const langDropdown = document.getElementById('languageDropdown');
        if (langDropdown) {
            const langNames = {
                'en': '🇬🇧 English',
                'hi': '🇮🇳 हिन्दी',
                'bn': '🇧🇩 বাংলা',
                'te': '🇮🇳 తెలుగు',
                'ta': '🇮🇳 தமிழ்'
            };
            const langText = langDropdown.querySelector('.d-none');
            if (langText) {
                langText.textContent = langNames[lang] || 'Language';
            }
        }
        
        // Update dropdown checkmarks
        document.querySelectorAll('.dropdown-menu .dropdown-item').forEach(item => {
            const check = item.querySelector('.fa-check');
            if (check) {
                check.remove();
            }
        });
        
        const selectedItem = document.querySelector(`.dropdown-item[onclick*="'${lang}'"]`);
        if (selectedItem && !selectedItem.querySelector('.fa-check')) {
            const check = document.createElement('i');
            check.className = 'fas fa-check me-2';
            selectedItem.prepend(check);
        }
        
        // Show success notification
        showToast(trans.language_changed || 'Language changed successfully', 'success');
        
        // Speak welcome message in new language
        if (typeof speakText === 'function') {
            setTimeout(() => {
                speakText(trans.welcome + ' to Smart Crop Advisory', lang);
            }, 500);
        }
        
        console.log('✅ Language changed to:', lang);
        
    } catch (error) {
        console.error('❌ Language change error:', error);
        showToast('Error changing language', 'error');
    }
}

// Auto-load saved language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage');
    if (savedLang && savedLang !== 'en') {
        console.log('🌐 Loading saved language:', savedLang);
        setTimeout(() => {
            changeLanguage(savedLang);
        }, 500);
    }
});

// Helper function for toast notifications
function showToast(message, type = 'info') {
    console.log(`📢 ${type.toUpperCase()}: ${message}`);
    
    const toastContainer = document.getElementById('toastContainer') || document.body;
    
    const toast = document.createElement('div');
    toast.className = `alert alert-${type} alert-dismissible fade show`;
    toast.style.cssText = 'position: fixed; bottom: 20px; right: 20px; z-index: 9999; min-width: 300px;';
    toast.innerHTML = `
        ${message}
        <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
    `;
    
    toastContainer.appendChild(toast);
    
    setTimeout(() => {
        toast.classList.remove('show');
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}

// Export for use in other scripts
if (typeof window !== 'undefined') {
    window.changeLanguage = changeLanguage;
    window.translations = translations;
}
